package uk.ac.diamond.daq.beamline.k11.diffraction.view.summary.editor;

import java.text.DecimalFormat;
import java.util.List;

/**
 * Represents a set of {@link Segment}s belonging to a common structure, i.e. an array.
 *
 * @param <T> the numeric structure represented by this group
 *
 * @author Maurizio Nagni
 */
public interface SegmentGroup<T> {
	/**
	 * The group numeric string format
	 *
	 * @return a formatted instance
	 */
	public DecimalFormat getDecimalFormat();
	/**
	 * The {@code Segment} belonging to this group
	 *
	 * @return a list of segments
	 */
	public List<Segment<T>> getSegments();
	/**
	 * Traversing all the group segments, recreate the structure represented by this group
	 *
	 * @return the original structure
	 */
	public T getValues();
	/**
	 * Persist the numerical structure. After calling this method the the numerical structure should be equivalent to the one
	 * generated by {@link #getValues()}
	 */
	public void persist();
	/**
	 * Defines when the group is editable
	 *
	 * @return {@code true} when editable, otherwise {@code false}
	 */
	public boolean isEditable();
}